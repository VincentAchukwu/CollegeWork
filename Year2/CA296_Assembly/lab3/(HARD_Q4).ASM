ORG C0
DB "AEQUIOAS"
DB 0				; MARKS END OF STRING


ORG 0
MOV AL,FF
PUSH AL				; MARK AS START OF STACK FOR APPEND (BELOW)
MOV AL,0
MOV BL,C0

JMP LOOP
LOOP:
	MOV CL,[BL]		; LOOP CHECKS FOR VOWELS
	CMP CL,41
	JZ RISE			; IF VOWEL, EXECUTE RISE		
	CMP CL,45
	JZ RISE
	CMP CL,49
	JZ RISE
	CMP CL,4F
	JZ RISE
	CMP CL,55
	JZ RISE
	MOV [BL],AL		; CLEARS VDU (SAME AS DB 0, EXCEPT W/OUT IT)
	INC BL
	CMP CL,0		; CHECK IF WE REACHED END OF STRING
	JNZ LOOP

	MOV BL,C0		; MADE IT EASIER FOR APPEND LABEL TO WORK
	POP AL	
	JMP APPEND

RISE:				; PUSH VOWELS TO STACK
	PUSH CL
	MOV [BL],AL
	INC DL
	INC BL
	JMP LOOP
	
APPEND:
	MOV [BL],AL
	INC BL
	POP AL
	CMP AL,FF		; CHECK WE'VE REACHED START OF STACK
	JNZ APPEND
	END

