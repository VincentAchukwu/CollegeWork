ORG 60
DB "382448958298290149035"		; RANDOM NUMBERS
DB 20				; END OF NUMBERS (MARKER)

ORG 0
MOV AL,60			; START INDEX OF NUMBERS
PUSH AL				; SAVE AL
RANGE:
	MOV BL,[AL]		; INC BL UNTIL WE REACH END OF "LIST" OF NUMS
	CMP BL,20
	JZ DONE
	INC AL
	JMP RANGE
DONE:
	PUSH AL			; NEW AL BECOMES BL (END RANGE)
	POP BL
	POP AL			; AL RESTORED


LOOP:
	CALL 1B			; CALLS PROCEDURE
	JMP LOOP
MOV DL,[AL]			; DL CONTAINS VALUE IN RANGE
SUB DL,30			; CONVERT FROM ASCII
INC AL				; INCREMENT INDEX
PUSH DL				; SAVE DL BEFORE USING MOD
MOD DL,2			; IF EVEN, DO NOTHING, ELSE, SUM ODDS INTO CL
JZ EVEN

ODD:
	POP DL
	ADD CL,DL
	CMP AL,BL		; CHECKHAVE WE REACHED END
	JZ FINISHED
	RET

EVEN:
	POP DL
	CMP AL,BL
	JZ FINISHED
	RET

FINISHED:
	END
