	MOV CL,[BL]	; PUT NUM IN [BL] TO CL
	MOD CL,2	; CHECK IF ODD/EVEN
	JNZ ODD		; JMP TO ODD IF OPERATION = 1 (ZERO FLAG NOT SET)
	JMP ODDMSG
	INC BL		; INC BL
	JNO LOOP	; WHILE HASN'T PASSED FF (I THINK YOU USE JNO)

ODD:
