; SETS WHERE IN VDU
MOV AL,C0			; CL NOW POINTER

; SETS CHARACTER TO PRINT (E.G #)
MOV BL,23			; STORE IT INTO DL

CALL 50				; GO TO MEM LOC 50 TO DO PROCEDURE
ADD AL,3F			; THEN DO THIS TO MOVE TO FF (HARDCODING 101)
CALL 50
HALT				; ENDS PROGRAM

ORG 50
PUSHF				; SAVE FLAGS AND REGISTERS
PUSH AL
MOV [AL],BL			; MOVE CHARACTER INTO THE VDU
POP AL
POPF
RET				; GO BACK TO WHERE WE CALLED

END				; PUTS A SMILE ON THE ASSEMBLER'S FACE :)