MOV AL,41		; MOVE LETTER A CAP TO AL
MOV BL,C0		; C0 TO BL FOR REFERENCE
JMP LOOP		; START LOOP
LOOP:
	MOV [BL],AL	; MOVE LETTER VALUE INTO [BL] WHICH HOLDS VALUE OF C0
	INC AL		; GET NEXT LETTER
	INC BL		; GET NEXT ADDRESS FOR VDU
	CMP AL,5B	; KEEP GOING WHILE IT'S A LETTER
	JNZ LOOP	; JUMP LOOP
	END