IN 0
JMP START
ORG 20
DB FB
DB 0B
DB B7
DB 9F
DB 4F ; 4
DB DD
DB FD
DB 8B
DB FF
DB CF

START:
	CMP AL,71		; IF "q", FINISHED
	JZ FINISHED
MOV CL,30
IS_DIGIT:			; CHECK IF NUMBER IS ENTERED
	CMP AL,CL
	JZ GOOD			; IF SO, WE'RE GOOD
	INC CL			; ELSE KEEP GOING
	CMP CL,3A
	JNZ IS_DIGIT
	IN 0			; NUMBER WASN'T ENTERED, GET NEW NUMBER
	JMP START		; START AGAIN
GOOD:
	PUSH AL			; SAVE AL
	AND AL,F0		; SAVE LEFT BITS
	DIV AL,10		; DIVIDE BY 10 TO GET IT AS ONE DIGIT
	POP BL			; DO SAME THING FOR BL
	AND BL,0F

OK:
	MOV CL,1F		; STARTING POINT HERE AS IT WORKS WITH MY LOOP STRUCTURE
	ADD AL,20		; CORRESPONDING INDEX TO NUMBERS
	ADD BL,20
LEFT_CHECK:
	INC CL
	CMP AL,CL		; CHECK IF WE MATCHED NUMBER TO INDEX
	JNZ LEFT_CHECK
	CALL 6F			; IF WE HAVE, WE DISPLAY IT
	MOV CL,1F		; THEN DO SAME FOR RIGHT DIGIT
RIGHT_CHECK:
	INC CL
	CMP BL,CL
	JNZ RIGHT_CHECK
	CALL 77
	;JZ DISPLAY_RIGHT
	;INC CL
	;CMP CL,2A
	JMP FINISHED

DISPLAY_LEFT:
	MOV AL,[CL]
	DEC AL
	OUT 02
	RET			; GO BACK TO CHECK RIGHT DIGIT

DISPLAY_RIGHT:
	MOV AL,[CL]
	OUT 02
	RET

FINISHED:
	END
