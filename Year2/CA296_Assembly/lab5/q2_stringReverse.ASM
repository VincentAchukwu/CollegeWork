ORG C0
DB "HeLlO"
DB 0

ORG 00
MOV AL,FF
PUSH AL
MOV AL,C0
MOV CL,D0
LOOP:
	MOV BL,[AL]
	CMP BL,0
	JZ SETUP_REVERSE
	PUSH BL
	INC AL
	JMP LOOP

SETUP_REVERSE:
	POP AL

REVERSE:
	MOV [CL],AL
	INC CL
	POP AL
	CMP AL,FF
	JZ ENDING
	JMP REVERSE

ENDING:
	END