SUB AL,30
;OUT 07			; FOR SIMPLE KEYBOARD (NEED INT THO I THINK)
MOV BL,0
FIRST_NUM:
	CMP AL,BL
	JZ SAVE
	INC BL
	CMP BL,A
	JNZ FIRST_NUM
	IN 0
	SUB AL,30
	MOV BL,0
	JMP FIRST_NUM

SAVE:
	PUSH AL
	POP DL
	IN 0
	SUB AL,30
	MOV BL,0
	JMP NEXT_NUM

NEXT_NUM:
	CMP AL,BL
	JZ MULT
	INC BL
	CMP BL,A
	JNZ NEXT_NUM
	IN 0
	SUB AL,30
	MOV BL,0
	JMP NEXT_NUM

MULT:
	;MOV CL,C0		; WHY NOT
	MUL AL,DL
	;ADD AL,30		; ONLY FOR VDU CHECKING COS WHY NOT
	;MOV [CL],AL		; WHY NOT
	END